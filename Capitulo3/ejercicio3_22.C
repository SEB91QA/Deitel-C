/*Programa que muestra 
la diferencia entre el predecremento
y el posdecremento*/


#include<stdio.h>



/*Inicio de la funci√≥n main*/


int main(){


   int a;
   int b;

   /*Preincremento*/
   a = 5;
   printf("%d\n", a);
   printf("%d\n", --a);
   printf("%d\n\n", a);

   b = 5;
   printf("%d\n", b);
   printf("%d\n", b--);
   printf("%d\n\n", b);

   return 0;





}
